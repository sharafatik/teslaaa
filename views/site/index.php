Tesla's AC system eventually caught the attention of American engineer and business man George Westinghouse, who was seeking a solution to supplying the nation with long-distance power. Convinced that Tesla's inventions would help him achieve this, in 1888 he purchased his patents for $60,000 in cash and stock in the Westinghouse Corporation. As interest in an alternating-current system grew, Tesla and Westinghouse were put in direct competition with Thomas Edison, who was intent on selling his direct-current system to the nation. 
